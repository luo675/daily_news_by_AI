def normalize_lang(value: str | None) -> str | None:
    if value is None:
        return None
    normalized = str(value).strip().lower()
    if normalized in SUPPORTED_LANGS:
        return normalized
    return None


def get_requested_lang(request: Request) -> str | None:
    return normalize_lang(request.query_params.get("lang"))


def get_persisted_lang(request: Request) -> str | None:
    return normalize_lang(request.cookies.get(LANG_COOKIE_NAME))


def resolve_lang(request: Request) -> str:
    return get_requested_lang(request) or get_persisted_lang(request) or DEFAULT_LANG


def t(lang: str, key: str, default: str | None = None) -> str:
    normalized_lang = normalize_lang(lang) or DEFAULT_LANG
    default_copy = COPY.get(DEFAULT_LANG, {})
    value = COPY.get(normalized_lang, {}).get(key)
    if value is not None:
        return value
    fallback = default_copy.get(key)
    if fallback is not None:
        return fallback
    if default is not None:
        return default
    return f"[{key}]"


def build_lang_switch_url(request: Request, target_lang: str) -> str:
    normalized_target = normalize_lang(target_lang) or DEFAULT_LANG
    query_items = [(key, value) for key, value in parse_qsl(request.url.query, keep_blank_values=True) if key != "lang"]
    query_items.append(("lang", normalized_target))
    query = urlencode(query_items)
    return str(request.url.path) if not query else f"{request.url.path}?{query}"


@dataclass(frozen=True)
class WebI18nContext:
    request: Request
    lang: str

    def text(self, key: str, default: str | None = None) -> str:
        return t(self.lang, key, default)

    def switch_url(self, target_lang: str) -> str:
        return build_lang_switch_url(self.request, target_lang)
